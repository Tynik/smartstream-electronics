/**
 * A union type representing commonly used keyboard key codes.
 * These key codes can be used to identify specific keyboard inputs in event handling.
 */
export type KeyboardEventCode =
  | 'Escape'
  | 'ArrowRight'
  | 'ArrowLeft'
  | 'ArrowUp'
  | 'ArrowDown'
  | 'Enter'
  | 'NumpadEnter'
  | 'Tab'
  | 'Space';
